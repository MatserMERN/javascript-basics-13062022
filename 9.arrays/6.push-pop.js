var numbers = [1,2,3]

// push - Will add the elements at the end of an array
numbers.push(4)
numbers.push(5)
numbers.push(6)

// unshift - Will add the elements at the begining of an array
numbers.unshift(0)
numbers.unshift(-1)
numbers.unshift(-2)

// pop - Will remove the elements at the end of an array 
numbers.pop()
numbers.pop()
numbers.pop()

//shift - Will remove the elements at the begining of an array
numbers.shift()
numbers.shift()
numbers.shift()

console.log(numbers)